preço = float(input('Digite o preço do produto: R$ '))
novo = preço - (preço * 5 / 100)
print(f'O preço atual é R${preço} e seu preço com 5% de desconto é R${novo}! ')

#para calcular porcentagem é: O "preço" (* x / 100) x é o valor que vai nele, por exemplo '5'!!!

